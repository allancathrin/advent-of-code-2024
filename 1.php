    sort($left, SORT_NUMERIC);
    sort($right, SORT_NUMERIC);

    for ($i = 0; $i < count($left); $i++) {
        $count += abs($left[$i] - $right[$i]);
    }

    echo sprintf('Step 1 : %s', $count);
}

function step2(): void
{
