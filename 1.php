    $rightCount = [];
    $left = left();
    sort($left, SORT_NUMERIC);

    foreach (right() as $value) {
        $rightCount[$value] = isset($rightCount[$value]) ? ++$rightCount[$value] : 1;
    }

    for ($i = 0; $i < count($left); $i++) {
        $count += $left[$i] * ($rightCount[$left[$i]] ?? 0);
    }

    echo sprintf('Step 2 : %s', $count);
}

